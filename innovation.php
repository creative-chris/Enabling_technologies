        <h3>What is artificial intelligence (AI)?</h3><br>
        <br>
        <p>Artificial intelligence is the simulation of human intelligence processes by machines, especially computer systems. Specific applications of AI include expert systems, natural language processing, speech recognition and machine vision.
        </p>
